    public void agregarCarreras(Collection<CarreraDTO> carreras) {
        for (CarreraDTO carrera : carreras) {
            agregarFila(carrera);
        }
    }
    
    private void agregarFila(CarreraDTO d) {
        Object[] datos = {d.getCodigo(), d.getNombre()};
        DefaultTableModel dtm = (DefaultTableModel) this.jTableCarreras.getModel();
        dtm.addRow( datos );
    }

    public void addConsultarAlumnosListener(MouseListener ml) {
        this.jTableCarreras.addMouseListener(ml);
    }

    public int getCodCarreraSeleccionado() {
        int nroFila = this.jTableCarreras.getSelectedRow();
        return (int) this.jTableCarreras.getValueAt(nroFila, 0);
    }
